// [P-05 FIX] Cache locale — reduced to 500ms for responsive language switching
let _cachedLocale: string | null = null;
let _localeCacheTime = 0;
const LOCALE_CACHE_MS = 500; // Re-read cookie every 0.5s — fast enough for dynamic switching

function getCachedLocale(): string {
    const now = Date.now();
    if (_cachedLocale && (now - _localeCacheTime) < LOCALE_CACHE_MS) {
        return _cachedLocale;
    }
    _cachedLocale = 'ar';
    if (typeof document !== 'undefined') {
        const match = document.cookie.match(/(?:^|;\s*)locale=(\w+)/);
        if (match?.[1] === 'en') _cachedLocale = 'en';
    }
    _localeCacheTime = now;
    return _cachedLocale;
}

/** Reset the locale cache — call this when the user toggles the language */
export function resetLocaleCache() {
    _cachedLocale = null;
    _localeCacheTime = 0;
}

/**
 * ta() — Auto-translate hardcoded Arabic strings.
 * This is a STANDALONE function (not a hook) that reads the locale from cookies.
 * 
 * Usage:
 *   import { ta } from '@/i18n/auto-translations';
 *   <h1>{ta('إدارة التقارير')}</h1>   // Shows English when locale=en
 *   <h1>{ta('إدارة التقارير', 'Report Management')}</h1>   // With explicit override
 * 
 * Can be used:
 * - Inside components
 * - Outside components (module-level constants)
 * - In utility functions
 * - Anywhere in the codebase
 */
export function ta(arabicText: string, englishOverride?: string): string {
    const locale = getCachedLocale();
    
    if (locale === 'ar') return arabicText;
    if (englishOverride) return englishOverride;
    return autoTranslations[arabicText] || arabicText;
}

/**
 * tDate() — Locale-aware date formatting helper.
 * Uses the same locale detection as ta().
 * 
 * Usage:
 *   import { tDate } from '@/i18n/auto-translations';
 *   <span>{tDate('2026-01-15')}</span>
 *   <span>{tDate(entry.date)}</span>
 */
export function tDate(dateStr: string | null | undefined, fallback: string = '—'): string {
    if (!dateStr) return fallback;
    const locale = getCachedLocale();
    return new Date(dateStr).toLocaleDateString(locale === 'en' ? 'en-US' : 'ar-SA');
}
